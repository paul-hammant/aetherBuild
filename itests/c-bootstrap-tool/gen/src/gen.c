/* A miniature of mquickjs's mqjs_stdlib: a code-generator tool.
   Run with no args  -> prints a values header.
   Run with "-names" -> prints a names header.
   Its stdout is captured by c.generated_header into a .h file that
   the main program then #includes. */
#include <stdio.h>
#include <string.h>

int main(int argc, char **argv) {
    int names = (argc > 1 && strcmp(argv[1], "-names") == 0);
    printf("/* generated by c-bootstrap-tool gen — do not edit */\n");
    if (names) {
        printf("static const char *GEN_NAMES[] = "
               "{ \"alpha\", \"beta\", \"gamma\" };\n");
        printf("#define GEN_NAMES_COUNT 3\n");
    } else {
        printf("#define GEN_MAGIC   0xACFB\n");
        printf("#define GEN_VERSION 7\n");
        printf("#define GEN_ANSWER  42\n");
    }
    return 0;
}
